	<h1>Strengths</h1>
	</div>
	<ul style="font-size:20px">
		<li>Easy to learn, debug, customize and configure files</li>
		<li>Has Documentation making it easy to learn whole language</li>
		<li>Large collection of libraries</li>
		<li>Compatible with almost all platforms, operating systems and web servers</li>
		<li>Lightweight, fast (execution time) and reliable</li>
		<li>Hassle-free migration from server to server</li>
		<li>Good security (data encryption)</li>
	</ul>
</div>

<div class="container">
	<div class="page-header">
	<h1>Weaknesses</h1>
	</div>
	<ul style="font-size:20px">
		<li>Will run on PHP5 but its core is mainly based on PHP4 and doesn't use features of PHP5 well</li>
		<li>Doesn't have regular releases (there are large periods of time between each release)</li>
		<li>Is not community-driven but company-driven</li>
		<li>Is PHP based only and not very object-oriented in some parts</li>
		<li>Has no built-in ORM (object-relational mapping)</li>
	</ul>
</div>

